export function translate(language, key, vars = {}) {
  const langPack = translations[language] || translations[DEFAULT_LANGUAGE];
  const fallback = translations[DEFAULT_LANGUAGE][key] || key;
  const raw = langPack[key] || fallback;
  return Object.keys(vars).reduce((text, varKey) => text.replaceAll(`{${varKey}}`, String(vars[varKey])), raw);
}
